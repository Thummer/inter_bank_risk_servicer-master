                21.融资性担保公司办理融资性担保业务，应当与被担保人约定在担保期间可持续获得相关信息并有权对相关情况进行核实。——《融资性担保公司管理暂行办法》<br>
                22.融资性担保公司与债权人应当建立担保期间被担保人相关信息的交换机制，加强对被担保人的信用辅导和监督，共同维护双方的合法权益。——《融资性担保公司管理暂行办法》<br>
                23.融资性担保公司应当按照监管部门的规定，将公司治理情况、财务会计报告、风险管理状况、资本金构成及运用情况、担保业务总体情况等信息告知相关债权人。——《融资性担保公司管理暂行办法》<br>
                24.融资性担保公司应当按照规定及时向监管部门报送经营报告、财务会计报告、合法合规报告等文件和资料。融资性担保公司向监管机构提交的各类文件和资料，应当真实、准确、完整。——《融资性担保公司管理暂行办法》<br>
