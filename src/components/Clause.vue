                1.具有代为清偿债务能力的法人、其他组织或者公民，可以作保证人。——《担保法》<br>
                2.国家机关不得为保证人，但经国务院批准为使用外国政府或者国际经济组织贷款进行转贷的除外。——《担保法》<br>
                3.企业法人的分支机构、职能部门不得为保证人。企业法人的分支机构有法人书面授权的，可以在授权范围内提供保证。——《担保法》<br>
                4.任何单位和个人不得强令银行等金融机构或者企业为他人提供保证；银行等金融机构或者企业对强令其为他人提供保证的行为，有权拒绝。——《担保法》<br>
                5.同一债务有两个以上保证人的，保证人应当按照保证合同约定的保证份额，承担保证责任。没有约定保证份额的，保证人承担连带责任，债权人可以要求任何一个保证人承担全部保证责任，保证人都负有担保全部债权实现的义务。已经承担保证责任的保证人，有权向债务人追偿，或者要求承担连带责任的其他保证人清偿其应当承担的份额。<br>
                6.保证人与债权人应当以书面形式订立保证合同。——《担保法》<br>
                7.保证人与债权人可以就单个主合同分别订立保证合同，也可以协议在最高债权额限度内就一定期间连续发生的借款合同或者某项商品交易合同订立一个保证合同。——《担保法》<br>
                8.当事人在保证合同中约定保证人与债务人对债务承担连带责任的，为连带责任保证。<br>
                9.连带责任保证的债务人在主合同规定的债务履行期届满没有履行债务的，债权人可以要求债务人履行债务，也可以要求保证人在其保证范围内承担保证责任。——《担保法》<br>
                10.当事人对保证方式没有约定或者约定不明确的，按照连带责任保证承担保证责任。——《担保法》<br>
                11.一般保证和连带责任保证的保证人享有债务人的抗辩权。债务人放弃对债务的抗辩权的，保证人仍有权抗辩。抗辩权是指债权人行使债权时，债务人根据法定事由，对抗债权人行使请求权的权利。——《担保法》<br>
                12.保证担保的范围包括主债权及利息、违约金、损害赔偿金和实现债权的费用。保证合同另有约定的，按照约定。当事人对保证担保的范围没有约定或者约定不明确的，保证人应当对全部债务承担责任。——《担保法》<br>
                13.保证期间，债权人依法将主债权转让给第三人的，保证人在原保证担保的范围内继续承担保证责任。保证合同另有约定的，按照约定。——《担保法》<br>
