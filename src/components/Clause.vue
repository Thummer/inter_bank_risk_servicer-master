                16.融资性担保公司的融资性担保责任余额不得超过其净资产的10倍。——《融资性担保公司管理暂行办法》<br>
                17.融资性担保公司以自有资金进行投资，限于国债、金融债券及大型企业债务融资工具等信用等级较高的固定收益类金融产品，以及不存在利益冲突且总额不高于净资产20％的其他投资。——《融资性担保公司管理暂行办法》<br>
                18.融资性担保公司不得为其母公司或子公司提供融资性担保。——《融资性担保公司管理暂行办法》<br>
                19.融资性担保公司应当按照当年担保费收入的50％提取未到期责任准备金，并按不低于当年年末担保责任余额1％的比例提取担保赔偿准备金。担保赔偿准备金累计达到当年担保责任余额10％的，实行差额提取。差额提取办法和担保赔偿准备金的使用管理办法由监管部门另行制定。——《融资性担保公司管理暂行办法》<br>
